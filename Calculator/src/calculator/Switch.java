package calculator;

public class Switch {

	public static void main(String[] args) {
		
	char grade = 'A';
	
	switch(grade) {
	 
	case 'A':
		System.out.println("You scored an A. You did Excillent.");
		break;
		
	case 'B':
		System.out.println("You scored a B.You did good.");
		break;
		
	case 'C':
		System.out.println("You scored a C.You did okey.");
		break;
		
	case 'D':
		System.out.println("You scored poorely");
		break;
		
	case 'F':
		System.out.println("You Failed");
		break;
		
default:
 	System.out.println("You made an invalid entry");
 	
		
	
	
			
	}
	}
	
}
