package calculator;

public class Loop {
	public static void main(String[] args) {
		int i;
		
		for (i = 1; i <= 10;i++) {
			
			System.out.println("This is loop number: " + i);
			
			//if(i == 4) this method is use to finding out the particular data from 10 thousands
			if (i == 4)
			break;
			
		}
		
		System.out.println("The looping is done!.");
	}

}
